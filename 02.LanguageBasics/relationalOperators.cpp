#include<iostream>

using namespace std;

int main(){

    int num1=6;
    int num2=3;

    cout<<"is Equals to: " << (num1==num2) <<endl;
    cout<< "not Equals to: " << (num1!=num2) <<endl;
    cout<<"greater Than: "<< (num1>num2) <<endl;
    cout<<"less Than: "<< (num1<num2) <<endl;
    cout<<"greater Than Equals to: "<< (num1>=num2) <<endl;
    cout<<"less Than Equals to: "<< (num1<=num2) <<endl;

    return 0;

}